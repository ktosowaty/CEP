package view;

public class PoliceView {
    private boolean serviceStatus;
    private boolean theftStatus;
    private boolean insuranceStatus;


    public boolean isServiceStatus() {
        return serviceStatus;
    }

    public void setServiceStatus(boolean serviceStatus) {
        this.serviceStatus = serviceStatus;
    }

    public boolean isTheftStatus() {
        return theftStatus;
    }

    public void setTheftStatus(boolean theftStatus) {
        this.theftStatus = theftStatus;
    }

    public boolean isInsuranceStatus() {
        return insuranceStatus;
    }

    public void setInsuranceStatus(boolean insuranceStatus) {
        this.insuranceStatus = insuranceStatus;
    }


}
